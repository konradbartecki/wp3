                <p class="lead">Dlaczego realizujemy projekt?</p>
                <p>Idea projektu "Wirtualne Powiaty. Budowa Społeczeństwa Informacyjnego na Lubelszczyźnie. Etap III" była prosta. Wykorzystanie nowoczesnych technologii, dzięki którym mieszkańcy obszarów wiejskich zyskają możliwość „połączenia się ze światem”. Wychodzimy naprzeciw potrzebom mieszkańców regionu nie mających dostępu do Internetu w miejscowościach oddalonych od większych miast. Chcemy aby każdy mieszkaniec powiatu łęczyńskiego, włodawskiego i świdnickiego oraz wszyscy odwiedzający nasz region mieli możliwość wyszukania potrzebnych informacji czy też sprawdzenia skrzynki e-mail. Mamy nadzieję, że realizacja projektu doprowadzi do zwiększenia atrakcyjności inwestycyjnej i turystycznej regionu a także rozwoju lokalnej przedsiębiorczości. Pełna informatyzacja to prosty i szybki sposób na poruszanie się po wirtualnym świecie. </p>
                <p class="lead">Co jest celem projektu?</p>
                <p>Głównym celem projektu "Wirtualne Powiaty. Budowa Społeczeństwa Informacyjnego na Lubelszczyźnie. Etap III" jest budowa infrastruktury telekomunikacyjnej, pozwalającej mieszkańcom miast i przede wszystkim mieszkańcom  obszarów wiejskich korzystanie z dostępu do szybkiego i taniego Internetu. Budowa infrastruktury pozwoli również na zwiększenie dostępności usług elektronicznych, a także podniesie atrakcyjność obszarów dla potencjalnych inwestorów. Dzięki konkretnym środkom kierowanym z budżetu Unii Europejskiej do samorządów rozwój tego typu inwestycji jest możliwy. Główny zamierzeniem współpracujących ze sobą samorządów z terenu powiatu łęczyńskiego, świdnickiego i włodawskiego jest to, aby do 2012 roku każdy użytkownik miał możliwość dostępu do krajowych i światowych usług, zasobów informacji i wiedzy za pomocą Internetu.</p>
                <p class="lead">Co powstanie w ramach projektu?</p>
                <p>W wyniku realizowanego projektu zostanie zbudowana infrastruktura telekomunikacyjna. W jej skład będzie wchodziła  regionalna sieć szerokopasmowa i budynek Centrum Zarządzania Siecią, a w nim serwerownia, telecentrum i Punkt Informacji Turystycznej.</p>
<p>Sieć szerokopasmowa obejmie swym zasięgiem trzy powiaty: łęczyński, świdnicki i włodawski, w sumie ponad 2 358,29 km2 powierzchni. Uzbrojenie terenu w infrastrukturę szerokopasmową zwiększy jego atrakcyjność i konkurencyjność względem innych regionów.  W mieście, ze względu na zwiększą gęstość zabudowy zostanie wybudowana sieć w technologii światłowodowej. Powstanie osiem kilometrów światłowodu dzięki, któremu potencjalni użytkownicy będą otrzymywać szybki Internet od operatorów. Światłowody jako doskonałe medium transmisji danych to obecnie jedna z najlepszych i najpopularniejszych technologii.</p>
<p>Oczywiście idealnym rozwiązaniem byłoby, gdyby do każdego użytkownika dostarczyć Internet z wykorzystaniem światłowodu, jednak ze względu na koszty oraz czasochłonność inwestycji nie jest to możliwe. Dlatego też na obszarach wiejskich zostanie wybudowana sieć szerokopasmowa wykorzystująca typową dla tych obszarów technologię WiMAX .  Jest to technologia radiowa umożliwiająca dostęp do  Internetu na obszarach o względnie małej gęstości zabudowy  (mniej zurbanizowanych).</p>
<p>Centrum Zarządzania Siecią jak sama nazwa wskazuje będzie to miejsce zarządzania i nadzorowania całą siecią telekomunikacyjną. CZS będzie pełniło kilka podstawowych funkcji m.in. : monitorowaniw zdarzeń w sieci, szybkie wychwytywanie i diagnozowanie problemów technicznych. Każda awaria związana z utratą łączności będzie od razu sygnalizowana odpowiednim alarmem i usuwana. W budynku obok CZS znajdzie się serwerownia - pomieszczenie ze sprzętem sieciowym. Zapewni ona m.in. działanie systemu bibliotecznego i skrzynki podawczej. Dzięki możliwościom jakie daje posiadanie własnej serwerowni możliwe będzie także uruchomienie m.in. telefonii internetowej. </p>
<p>Pozostałe elementy to telecentra, w których bezpłatnie skorzystamy z Internetu oraz sieć hot-spotów. Dodatkowym efektem projektu będzie otwarcie w budynku, Punktu Informacji Turystycznej. </p>
                <p class="lead">Do kogo skierowany jest projekt?</p>
                <p>Projekt "Wirtualne Powiaty. Budowa Społeczeństwa Informacyjnego na Lubelszczyźnie. Etap III" skierowany jest do mieszkańców, turystów, instytucji publicznych i lokalnych operatorów telekomunikacyjnych z terenu trzech powiatów. Zyskają oni możliwość korzystania z szerokiego zakresu usług publicznych dostęnych drogą elektroniczną, bądź to bezpośrednio w telecentrum bądź dzięki sieci hot-spotów. Powstały w ramach projektu CZS pozwoli na przygotowanie instytucji publicznych do elektronicznego obiegu dokumentów, ich elektronicznej archiwizacji rozwoju systemu baz danych. Projekt powstał także z myślą o lokalnych  operatorach. Powstała infrastruktura pozwoli im rozpowszechniać sieć przekazu, a tym samym świadczyć szybsze usługi internetowe  klientom. Dla małych operatorów pojawia się więc szansa na pozyskanie nowych użytkowników, a tym samym rozszerzenie działalności.</p>
                <p class="lead">Skąd pozyskaliśmy środki na projekt?</p>
                <p>Projekt współfinansowany jest w 85 % ze środków Europejskiego Funduszu Rozwoju Regionalnego  w ramach Regionalnego Programu Operacyjnego Województwa Lubelskiego na lata 2007-2013. Całkowita wartość projektu to ponad 30 mln złotych. Wkład własny partnerów wynosi 15 % całego przedsięwzięcia.</p>

